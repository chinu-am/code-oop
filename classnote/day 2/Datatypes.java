public class Datatypes {
    public static void main(String args[]) {
        System.out.println("This is printed by print statement");
        System.out.println("Datatypes Example with New Values");

        int num = 45;
        System.out.printf("This is Integer %d\n", num);

        short numshort = 1200;
        System.out.printf("This is Short Integer %d\n", numshort);

        long numlong = 9876543210L;
        System.out.printf("This is Long Integer %d\n", numlong);

        float numdecimal = 9.81f;
        System.out.printf("This is Float %f\n", numdecimal);

        double numdouble = 12345.6789;
        System.out.printf("This is Double %f\n", numdouble);

        char charac = 'Z';
        System.out.printf("This is Character %c\n", charac);

        boolean sam = false;
        System.out.printf("This is Boolean %b\n", sam);
    }
}
